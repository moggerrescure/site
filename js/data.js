/* Helper: calculate age from date strings like "12.03.1918" */
function calcAge(born, died) {
  const parseY = s => { const m = (s||'').match(/(\d{4})/); return m ? +m[1] : null; };
  const b = parseY(born);
  const d = parseY(died) || new Date().getFullYear();
  return (b && d && d > b) ? d - b : null;
}

